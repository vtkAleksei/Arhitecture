Определить сложность следующих алгоритмов:


1. Поиск элемента массива с известным индексом
Сложность алгоритма = О(1)

2. Дублирование массива через foreach
Сложность алгоритма = О(n), где n количество элементов в массиве 1. Можно пройти одним циклом копируя с массива 1 значения в массив 2

3. Рекурсивная функция нахождения факториала числа.
Сложность алгоритма = О(n), т.к количество раз выполнения функции зависит от числа n